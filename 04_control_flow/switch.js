// SWITCH case will be useful further in REDUX.
// Basic Syntax of Switch case are as follows:-
// switch (key) {
//     case value:
        
//         break;

//     default:
//         break;
// }

// EXAMPLE to find Month:-
const month = 3
switch (month) {
    case 1:
        console.log(`january`);
        break;
    case 2:
        console.log(`february`);
        break;
    case 3:
        console.log(`march`);
        break;
    case 4:
        console.log(`march`);
        break;

    default:
        console.log(`default case match!`);
        break;
}

